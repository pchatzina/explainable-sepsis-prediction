"""
Module: signals.py
===================
Purpose: Process full-length ECG waveform signals from MIMIC-IV-ECG (Phase 2, Stage 2).

Pipeline Context:
  Executed AFTER records.py completes ECG metadata extraction.
  Loads WFDB .hea/.dat files → applies preprocessing → saves as .mat files.

Output Format (.mat files):
  - Filename: {study_id}.mat

Reference: https://github.com/Jwoo5/fairseq-signals/blob/master/scripts/preprocess/ecg/mimic_iv_ecg_signals.py
"""

import logging
import pandas as pd
from pathlib import Path

from fairseq_signals_scripts.preprocess.ecg.preprocess import (
    pipeline,
    postprocess_wfdb,
)
from src.utils.config import Config

SOURCE = "mimic_iv_ecg"
logger = logging.getLogger(__name__)


class MockArgs:
    """
    Mock argparse namespace for fairseq_signals pipeline.

    Key Parameters:
        raw_root, processed_root: Input/output directories
        skip: "segmented" (preserve full-length waveforms, don't chunk into 5s segments)
        desired_sample_rate: 500 Hz (standard rate for ECG foundation models)
        leads: Range(12) (standard 12-lead ECG)
        no_standardization: False (ENABLE z-score normalization)
        nb_workers: 8 (parallelism level)
    """

    def __init__(self):
        self.raw_root = Config.RAW_ECG_DIR
        self.processed_root = Config.PROCESSED_ECG_ROOT_DIR

        # Preserving whole-ECG logic (not segmented)
        self.skip = "segmented"
        self.segment_seconds = None

        self.manifest_file = None
        self.results_suffix = ""
        self.dataset_subset = None
        self.no_parallel = False
        self.nb_workers = 8
        self.no_standardization = False  # Ensures Z-score normalization is applied
        self.leads = [str(i) for i in range(12)]
        self.constant_lead_strategy = "zero"
        self.desired_sample_rate = 500


def postprocess_meta(meta: pd.DataFrame):
    """
    Clean up unnecessary columns before finalizing ECG metadata.
    """
    meta = meta.drop(["comments", "base_date", "base_time"], axis=1)
    return postprocess_wfdb(meta)


def main() -> None:
    """
    Main entry point: Process ECG waveforms using fairseq_signals.
    """
    Config.setup_logging()
    logger.info("Starting ECG waveform preprocessing...")

    args = MockArgs()

    # Load the records generated by records.py
    records_csv_path = args.processed_root / "records.csv"
    logger.info("Loading records from: %s", records_csv_path)
    records = pd.read_csv(records_csv_path)

    # Construct full paths for the waveforms
    records["path"] = records["path"].apply(lambda p: str(args.raw_root / p))

    records["save_file"] = records["study_id"].astype(str) + ".mat"
    logger.info("Mapped save files to study_id.")

    records["source"] = SOURCE
    records["dataset"] = SOURCE

    args.raw_root = str(args.raw_root)
    args.processed_root = str(args.processed_root)

    logger.info("Initiating fairseq_signals pipeline...")
    pipeline(
        args,
        records,
        SOURCE,
        postprocess_extraction={"meta": postprocess_meta},
    )
    logger.info("ECG waveform preprocessing complete.")


if __name__ == "__main__":
    main()
